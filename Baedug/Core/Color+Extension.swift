import Foundation
import UIKit
extension UIColor {
    static var customGray : UIColor {
        return UIColor(named: "customGray") ?? .white
    }
    static var customYellow : UIColor {
        return UIColor(named: "customYellow") ?? .white
    }
    static var customDarkyellow : UIColor {
        return UIColor(named: "customDarkyellow") ?? .white
    }
}
